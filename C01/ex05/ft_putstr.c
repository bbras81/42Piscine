#include <unistd.h>

void	ft_strlen(char *str)
{
	int	count;

	count = 0;
	while (str[count] != '\0')
	{
		write(1, &str[count], 1);
		count ++;
	}
}


#include <stdio.h>

int main()
{
	char *str = "Hello World!";

	ft_strlen(str);

	return (0);
}

